package org.cuacfm.concursos.api;

public class VotoCategoriaDto {

    private Long idCategoriaEdicion;
    private Long tres;
    private Long dos;
    private Long uno;

    public Long getIdCategoriaEdicion() {
        return idCategoriaEdicion;
    }

    public void setIdCategoriaEdicion(Long idCategoriaEdicion) {
        this.idCategoriaEdicion = idCategoriaEdicion;
    }

    public Long getTres() {
        return tres;
    }

    public void setTres(Long tres) {
        this.tres = tres;
    }

    public Long getDos() {
        return dos;
    }

    public void setDos(Long dos) {
        this.dos = dos;
    }

    public Long getUno() {
        return uno;
    }

    public void setUno(Long uno) {
        this.uno = uno;
    }

}
